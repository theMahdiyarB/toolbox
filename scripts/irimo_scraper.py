PERSIAN_DIGITS = str.maketrans("۰۱۲۳۴۵۶۷۸۹", "0123456789")

def to_latin(text: str) -> str:
    return text.translate(PERSIAN_DIGITS).strip() if text else ""

def parse_temp(text: str):
    m = re.search(r"-?\d+", to_latin(text))
    return int(m.group()) if m else None

def parse_float(text: str):
    m = re.search(r"-?\d+\.?\d*", to_latin(text))
    return float(m.group()) if m else None

def station_url(irimo_id: str) -> str:
    return f"https://irimo.ir/far/index.php?module=web_directory&wd_id=701&id={irimo_id}"

# ---------------------------------------------------------------------------
# HTTP
# ---------------------------------------------------------------------------
def fetch(url: str) -> BeautifulSoup | None:
    for attempt in range(1, MAX_RETRIES + 1):
        try:
            r = session.get(url, timeout=REQUEST_TIMEOUT)
            r.raise_for_status()
            r.encoding = "utf-8"
            return BeautifulSoup(r.text, "html.parser")
        except Exception as exc:
            log.warning("Attempt %d/%d failed for %s: %s", attempt, MAX_RETRIES, url, exc)
            if attempt < MAX_RETRIES:
                time.sleep(REQUEST_DELAY * attempt)
    log.error("Giving up on %s", url)
    return None

