def main():
    total = len(STATIONS)
    log.info("=== IRIMO Scraper v3 | %d stations | %d provinces ===",
             total, len(PROVINCE_STATIONS))
    start = time.time()
    records = []

    for i, irimo_id in enumerate(STATIONS, 1):
        name, province, _, _ = STATIONS[irimo_id]
        log.info("[%d/%d] %s — %s", i, total, name, province)
        time.sleep(REQUEST_DELAY)
        records.append(scrape_station(irimo_id))

    output = {
        "generated_at":  datetime.now(timezone.utc).isoformat(),
        "station_count": len(records),
        "stations":      records,
    }
    OUTPUT_FILE.write_text(
        json.dumps(output, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    elapsed = time.time() - start
    log.info("Done. %d stations → %s  (%.0fs)", len(records), OUTPUT_FILE, elapsed)

if __name__ == "__main__":
    main()