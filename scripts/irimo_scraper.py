def parse_current(div) -> dict:
    data = {}

    # Temperature — large font display in the left panel
    temp_div = div.find("div", style=lambda s: s and "font-size:48px" in s)
    if temp_div:
        data["temp_c"] = parse_temp(temp_div.get_text())

    # Condition text — medium font below temperature
    cond_div = div.find("div", style=lambda s: s and "font-size:18px" in s)
    if cond_div:
        cond = cond_div.get_text(strip=True)
        if cond and cond != "-":
            data["condition"] = cond

    # Wind direction — small font paragraph
    wind_div = div.find("div", style=lambda s: s and "font-size:12px" in s)
    if wind_div:
        m = re.search(r"جهت باد[:\s]*([\u0600-\u06FF\s]+)", wind_div.get_text(strip=True))
        if m:
            data["wind_direction"] = m.group(1).strip()

    # Key-value rows in the right panel
    for cap in div.find_all(class_="wet_s_wblk_caption"):
        label = cap.get_text(strip=True)
        # The value is the next sibling span/element with class wet_s_wblk_data
        # OR for min/max it's wet_s_wblk_data2
        data_el = cap.find_next_sibling()
        value = data_el.get_text(strip=True) if data_el else ""

        if "سرعت باد" in label:
            m = re.search(r"(\d+)\s*متر بر ثانیه", to_latin(value))
            data["wind_speed_ms"] = int(m.group(1)) if m else None
            m2 = re.search(r"(\d+)\s*کیلومتر بر ساعت", to_latin(value))
            data["wind_speed_kmh"] = int(m2.group(1)) if m2 else None

        elif "نم نسبی" in label:
            data["humidity_pct"] = parse_float(value)

        elif "دید افقی" in label:
            data["visibility"] = value if value and value != "-" else None

        elif "بارندگی 24" in label:
            if "صفر" in value:
                data["rainfall_24h_mm"] = 0.0
            else:
                data["rainfall_24h_mm"] = parse_float(to_latin(value))

        elif "بیشینه دما" in label:
            # wet_s_wblk_data2 comes immediately after this caption
            s2 = cap.find_next(class_="wet_s_wblk_data2")
            if s2:
                data["temp_max_c"] = parse_temp(s2.get_text())

        elif "کمینه دما" in label:
            # There are two wet_s_wblk_data2 elements; بیشینه owns the first.
            # Find both and take the second one for کمینه.
            all_d2 = div.find_all(class_="wet_s_wblk_data2")
            if len(all_d2) >= 2:
                data["temp_min_c"] = parse_temp(all_d2[1].get_text())
            elif all_d2:
                data["temp_min_c"] = parse_temp(all_d2[0].get_text())

    # Last update timestamp
    upd = div.find(class_="updateIcon")
    if upd:
        m = re.search(r"(\d{4}/\d{2}/\d{2}\s+\d{2}:\d{2})", to_latin(upd.get_text(strip=True)))
        data["last_updated"] = m.group(1) if m else None

    return data


def parse_forecast(div) -> list[dict]:
    """
    Each forecast day is a <td style='border:1px solid #BBCCEB;...'>
    Structure inside:
      div[font-size:14px] → period label (e.g. '24 ساعت اول')
      div[img]            → weather icon (skip)
      div[height:70px]    → condition text
      div[font-size:21px] → container for min/max
        div[float:right]  → min temp (with downL arrow)
        div[float:left]   → max temp (with upL arrow)
    """
    forecasts = []
    tds = div.find_all("td", style=lambda s: s and "border:" in s)

    for td in tds[:3]:
        day = {}

        # Period label
        period_div = td.find("div", style=lambda s: s and "font-size:14px" in s)
        if period_div:
            day["period"] = period_div.get_text(strip=True)

        # Condition description
        cond_div = td.find("div", style=lambda s: s and "height:70px" in s)
        if cond_div:
            day["condition"] = cond_div.get_text(strip=True)

        # Min/max container
        temp_container = td.find("div", style=lambda s: s and "font-size:21px" in s)
        if temp_container:
            min_div = temp_container.find("div", style=lambda s: s and "float:right" in s)
            max_div = temp_container.find("div", style=lambda s: s and "float:left" in s)
            if min_div:
                day["temp_min_c"] = parse_temp(min_div.get_text())
            if max_div:
                day["temp_max_c"] = parse_temp(max_div.get_text())

        if day:
            forecasts.append(day)

    return forecasts


def scrape_station(irimo_id: str) -> dict:
    name, province, lat, lon = STATIONS[irimo_id]
    url = station_url(irimo_id)

    result = {
        "irimo_id":   irimo_id,
        "name":       name,
        "province":   province,
        "url":        url,
        "lat":        lat,
        "lon":        lon,
        "scraped_at": datetime.now(timezone.utc).isoformat(),
        "current":    {},
        "forecast":   [],
    }

    soup = fetch(url)
    if not soup:
        result["error"] = "fetch_failed"
        return result

    cur = soup.find("div", id="divCurrentWeather")
    if cur:
        result["current"] = parse_current(cur)

    fut = soup.find("div", id="divFutureWeather")
    if fut:
        result["forecast"] = parse_forecast(fut)

    return result

